#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_content;

	if (!del || !f)
		return (NULL);
	new_list = NULL;
	while (lst)
	{
		if (!(new_content = ft_lstnew(f(lst->content))))
		{
			ft_lstclear(&new_content, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_content);
		lst = lst->next;
	}
	return (new_list);
}
